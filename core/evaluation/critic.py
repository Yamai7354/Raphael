import logging
from typing import Dict, Any
from agents.base import BaseAgent

logger = logging.getLogger(__name__)


class CriticAgent(BaseAgent):
    """
    An adversarial agent dedicated to peer-reviewing outputs generated by
    worker agents. It does not execute tasks, but rather evaluates them.
    """

    def __init__(self):
        super().__init__(
            agent_id="CriticAgent", capabilities=["review", "code_review", "logic_validation"]
        )

    async def execute(self, params: Dict[str, Any]) -> Dict[str, Any]:
        """
        Mock implementation of a peer-review AI pass over generated work.
        Requires `target_output` in the params.
        """
        target = params.get("target_output", {})

        # Simulated reasoning: If we injected a "flaw" flag, trap it.
        if "flaw" in target or target.get("quality") == "poor":
            logger.warning("CriticAgent REJECTED the target output due to logical flaws.")
            return self._standard_response(
                success=False,
                logs=["Adversarial review identified logic gaps.", "Feedback generated."],
                output={
                    "critique": "The executed output failed edge-case testing.",
                    "retry_recommended": True,
                },
            )

        logger.info("CriticAgent APPROVED target output.")
        return self._standard_response(
            success=True,
            logs=["Adversarial review complete.", "Output logic aligns with instructions."],
            output={"critique": "Approved without modification.", "retry_recommended": False},
        )
